package javaProject;

public class switchString {
	public static void main(String[] args) {
		
		string value="Three";
		
		switch (value) {
		case "One":{
			System.out.println("The value is "+1);
		}
		case "Two":{
			System.out.println("The value is "+2);
		}
		case "Three":{
			System.out.println("The value is "+3);
		}
		case "Four":{
			System.out.println("The value is "+4);
			
		}
		
		
		}
		
	}

}
